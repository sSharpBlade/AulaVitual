    private void btnModActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModActionPerformed
        if (txtCedula.getText().isEmpty() || txtNombre.getText().isEmpty() || txtApellido.getText().isEmpty() || txtCorreo.getText().isEmpty() || txtPassword.getPassword().equals("") || txtDireccion.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe llenar los campos habilitados");
        } else {
            this.admin.setCedula(txtCedula.getText());
            if (this.admin.verificarDNI()) {
                Verificador aux = new Verificador();
                if (aux.validate(txtCorreo.getText())) {
                    this.admin.setNombre(txtNombre.getText());
                    this.admin.setApellido(txtApellido.getText());
                    this.admin.setCorreo(txtCorreo.getText());
                    this.admin.setDireccion(txtDireccion.getText());
                    this.admin.setPas(String.valueOf(txtPassword.getPassword()));
                    this.admin.modificarUsuario();
                    this.vaciar();
                    this.admin.actualizarTablaTipo(tbDatos, this.getTipo());
                    this.botones(false);
                    btnAsignar.setEnabled(false);
                } else {
                    JOptionPane.showMessageDialog(null, "El correo ingresado no es valido");
                }
            }
        }
    }//GEN-LAST:event_btnModActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (0 == JOptionPane.showConfirmDialog(null, "Estas seguro de realizar esta accion, no habra marcha atras")) {

            this.admin.eliminarTipo();
        }
        this.vaciar();
        this.lista();
        this.admin.actualizarTablaTipo(tbDatos, this.getTipo());
        this.botones(false);
        btnAsignar.setEnabled(false);
    }//GEN-LAST:event_btnEliminarActionPerformed

