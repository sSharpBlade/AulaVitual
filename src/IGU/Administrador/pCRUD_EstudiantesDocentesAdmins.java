    private void txtCorreoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCorreoKeyTyped
        char c = evt.getKeyChar();
        if (!Character.isLetterOrDigit(c) && c != '.' && c != '-' && c != '_' && c != '@') {
            evt.consume();
        }
    }//GEN-LAST:event_txtCorreoKeyTyped

